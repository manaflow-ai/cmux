#!/usr/bin/env swift
// Regenerates Sources/WorkspaceGroupEmojiData.swift, the baked emoji dataset for the
// workspace group icon picker. Run from the repo root after a Unicode/toolchain update:
//
//     swift scripts/generate-workspace-group-emoji.swift
//
// Keywords come from the Unicode scalar names so the picker can search by name
// ("rocket" -> 🚀) without scanning Unicode ranges at runtime.
import Foundation

let commonEmoji = [
    "🚀", "💻", "🧠", "⚙️", "🔥", "✅", "📁", "🧪", "🎯", "✨", "⚡️", "⭐️",
    "🔧", "📌", "📝", "🔍", "🎨", "🧩", "📦", "🌐", "🔒", "💬", "🏁", "📊",
    "🛠️", "🧰", "📣", "💡", "🕹️", "🧵", "🪄", "🧱", "📎", "🗂️", "📚", "🔬"
]
let ranges: [ClosedRange<Int>] = [0x203C ... 0x3299, 0x1F000 ... 0x1FAFF]

func isEmojiCharacter(_ ch: Character) -> Bool {
    let scalars = ch.unicodeScalars
    let containsEmoji = scalars.contains { $0.properties.isEmoji }
    let containsPresentation = scalars.contains {
        $0.properties.isEmojiPresentation || $0.properties.isEmojiModifier ||
            $0.properties.isEmojiModifierBase || $0.value == 0xFE0F
    }
    let asciiOnly = scalars.allSatisfy { $0.value < 128 }
    return containsEmoji && (containsPresentation || !asciiOnly)
}

func keywords(for ch: Character) -> String {
    var parts: [String] = []
    for s in ch.unicodeScalars {
        if s.value == 0xFE0F || s.value == 0x200D { continue }
        if let n = s.properties.name { parts.append(n.lowercased()) }
    }
    let mapped = parts.joined(separator: " ").unicodeScalars.map {
        CharacterSet.alphanumerics.contains($0) ? Character($0) : " "
    }
    return String(mapped).split(separator: " ").joined(separator: " ")
}

var seen = Set<String>()
var entries: [(String, String)] = []
func add(_ ch: Character) {
    let v = String(ch)
    guard !seen.contains(v) else { return }
    seen.insert(v)
    entries.append((v, keywords(for: ch)))
}

for e in commonEmoji where e.count == 1 { add(e.first!) }
for r in ranges {
    for value in r {
        guard let s = Unicode.Scalar(value) else { continue }
        let ch = Character(s)
        guard isEmojiCharacter(ch) else { continue }
        add(ch)
    }
}

var out = """
// GENERATED by scripts/generate-workspace-group-emoji.swift. Do not edit by hand.
// Regenerate after Unicode updates: swift scripts/generate-workspace-group-emoji.swift

/// Baked, searchable emoji dataset for the workspace group icon picker.
///
/// Each entry pairs an emoji with its lowercased keyword text (from the Unicode scalar
/// names). Built once at dev time so the picker never scans Unicode ranges at runtime.
enum WorkspaceGroupEmojiData {
    /// `(value, keywords)` pairs in browse order (common emoji first).
    static let entries: [(value: String, keywords: String)] = [

"""
for (v, k) in entries {
    let ev = v.replacingOccurrences(of: "\\", with: "\\\\").replacingOccurrences(of: "\"", with: "\\\"")
    let ek = k.replacingOccurrences(of: "\\", with: "\\\\").replacingOccurrences(of: "\"", with: "\\\"")
    out += "        (\"\(ev)\", \"\(ek)\"),\n"
}
out += "    ]\n}\n"

try! out.write(toFile: "Sources/WorkspaceGroupEmojiData.swift", atomically: true, encoding: .utf8)
FileHandle.standardError.write("generated \(entries.count) emoji entries\n".data(using: .utf8)!)
