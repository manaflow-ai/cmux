// scripts/build-turn-diff-panel.ts
// Builds the Vite bundle and codegens a Swift string-literal file.
// Usage: bun scripts/build-turn-diff-panel.ts

import { $ } from "bun"
import { readFile, writeFile } from "node:fs/promises"
import { join, resolve } from "node:path"

const repoRoot = resolve(import.meta.dir, "..")
const panelDir = join(repoRoot, "panels", "turn-diff")
const swiftOut = join(repoRoot, "Sources", "Panels", "PerTurnDiff", "TurnDiffWebViewBundle.swift")

// Vite 5 + bun runtime has a crypto.getRandomValues incompatibility.
// Run vite via node directly (Node 20 available via nvm).
const nodeBin = join(panelDir, "node_modules", ".bin", "vite")
const nodePath =
  process.env.VITE_NODE_BIN ?? "/Users/waichong/.nvm/versions/node/v20.20.2/bin/node"

console.log("[turn-diff] vite build…")
await $`${nodePath} ${nodeBin} build`.cwd(panelDir).quiet()

const html = await readFile(join(panelDir, "dist", "index.html"), "utf8")

// Swift triple-quoted string literal escaping:
// - Backslashes: \ → \\
// - Triple-quote sequences: """ → "\""\"" (split with escaped middle)
const escaped = html
  .replace(/\\/g, "\\\\")
  .replace(/"""/g, '"\\""\\""')

const swift = `// SPDX-License-Identifier: GPL-3.0-or-later
// AUTOGENERATED — do not edit. Regenerate via: bun scripts/build-turn-diff-panel.ts
// Source: panels/turn-diff/

import Foundation

enum TurnDiffWebViewBundle {
    /// Single-file HTML bundle (HTML + inlined JS + inlined CSS) for the per-turn diff panel.
    static let html: String = """
${escaped}
"""
}
`

await writeFile(swiftOut, swift, "utf8")
console.log(`[turn-diff] wrote ${swiftOut} (${html.length} bytes html)`)
