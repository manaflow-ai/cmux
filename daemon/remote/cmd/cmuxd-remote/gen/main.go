// gen generates commands_generated.go by calling system.command_spec on the
// running cmux app via the relay socket.
//
// Usage:
//
//	go generate ./cmd/cmuxd-remote/
//
// Requires CMUX_SOCKET_PATH to be set, or ~/.cmux/socket_addr to be readable.
// After regenerating, commit commands_generated.go. CI runs:
//
//	go generate ./cmd/cmuxd-remote/ && git diff --exit-code
//
// to detect the relay drifting behind the Mac CLI.
package main

import (
	"encoding/json"
	"fmt"
	"go/format"
	"net"
	"os"
	"path/filepath"
	"sort"
	"strings"
	"text/template"
	"time"
)

// ── Wire types matching TerminalController+CLICommandSpec.swift ──────────────

type flagSpec struct {
	Name string `json:"name"`
	Type string `json:"type"` // "string" | "bool" | "repeatable"
}

type commandEntry struct {
	Method     string     `json:"method"`
	Flags      []flagSpec `json:"flags"`
	Positional string     `json:"positional,omitempty"`
	Aliases    []string   `json:"aliases,omitempty"`
}

type specResponse struct {
	Commands map[string]commandEntry `json:"commands"`
}

// ── Socket call ──────────────────────────────────────────────────────────────

func fetchSpec(socketPath string) (specResponse, error) {
	conn, err := net.DialTimeout("unix", socketPath, 5*time.Second)
	if err != nil {
		return specResponse{}, fmt.Errorf("dial %s: %w", socketPath, err)
	}
	defer conn.Close()

	req, _ := json.Marshal(map[string]any{
		"id":     "gen-1",
		"method": "system.command_spec",
		"params": map[string]any{},
	})
	conn.SetDeadline(time.Now().Add(10 * time.Second))
	if _, err := conn.Write(append(req, '\n')); err != nil {
		return specResponse{}, fmt.Errorf("write: %w", err)
	}

	buf := make([]byte, 1<<20)
	n, err := conn.Read(buf)
	if err != nil {
		return specResponse{}, fmt.Errorf("read: %w", err)
	}

	var envelope struct {
		OK     bool            `json:"ok"`
		Result json.RawMessage `json:"result"`
		Error  *struct {
			Code    string `json:"code"`
			Message string `json:"message"`
		} `json:"error"`
	}
	if err := json.Unmarshal(buf[:n], &envelope); err != nil {
		return specResponse{}, fmt.Errorf("parse response: %w", err)
	}
	if !envelope.OK {
		if envelope.Error != nil {
			return specResponse{}, fmt.Errorf("RPC error %s: %s", envelope.Error.Code, envelope.Error.Message)
		}
		return specResponse{}, fmt.Errorf("RPC returned ok=false")
	}

	var spec specResponse
	if err := json.Unmarshal(envelope.Result, &spec); err != nil {
		return specResponse{}, fmt.Errorf("parse spec: %w", err)
	}
	return spec, nil
}

func resolveSocket() (string, error) {
	if p := os.Getenv("CMUX_SOCKET_PATH"); p != "" {
		return p, nil
	}
	home, err := os.UserHomeDir()
	if err != nil {
		return "", err
	}
	addr, err := os.ReadFile(filepath.Join(home, ".cmux", "socket_addr"))
	if err != nil {
		return "", fmt.Errorf("CMUX_SOCKET_PATH not set and ~/.cmux/socket_addr not readable: %w", err)
	}
	return strings.TrimSpace(string(addr)), nil
}

// ── Code generation ──────────────────────────────────────────────────────────

const fileTemplate = `// Code generated by go generate (cmd/cmuxd-remote/gen/main.go); DO NOT EDIT.
// Source: system.command_spec via cmux v2 RPC.
// Regenerate: go generate ./cmd/cmuxd-remote/
// Add relay-specific overrides (paramKeyOverrides, positionalKey, etc.) in cli_overrides.go.

package main
{{if .Imports}}
import ({{range .Imports}}
	{{printf "%q" .}}{{end}}
)
{{end}}
var commands = []commandSpec{
{{- range .Commands}}
	{
		name:     {{printf "%q" .Name}},
		v2Method: {{printf "%q" .Method}},
		{{- if .NoParams}}
		noParams: true,
		{{- end}}
		{{- if .FlagKeys}}
		flagKeys: []string{ {{range .FlagKeys}}{{printf "%q" .}}, {{end}} },
		{{- end}}
		{{- if .BoolFlags}}
		boolFlags: []string{ {{range .BoolFlags}}{{printf "%q" .}}, {{end}} },
		{{- end}}
		{{- if .RepeatKeys}}
		repeatKeys: []string{ {{range .RepeatKeys}}{{printf "%q" .}}, {{end}} },
		{{- end}}
		{{- if .PositionalKey}}
		positionalKey: {{printf "%q" .PositionalKey}},
		{{- end}}
	},
{{- end}}
}
`

type commandData struct {
	Name          string
	Method        string
	NoParams      bool
	FlagKeys      []string
	BoolFlags     []string
	RepeatKeys    []string
	PositionalKey string
}

type templateData struct {
	Imports  []string
	Commands []commandData
}

func generate(spec specResponse, overrides map[string]commandOverrideDef) ([]byte, error) {
	// Build a stable-ordered list: use primary names only (skip aliases — they
	// share the same entry dict but we only want one commandSpec per command).
	// Determine primary names from entries that appear without an alias match.
	// The spec response includes aliases mapped to the same dict, so deduplicate
	// by method+flags identity; the shorter name is usually primary. For simplicity,
	// we rely on the generator caller to know which names are primaries — we skip
	// any name that appears in another entry's aliases list.
	aliasSet := make(map[string]bool)
	for _, entry := range spec.Commands {
		for _, a := range entry.Aliases {
			aliasSet[a] = true
		}
	}

	names := make([]string, 0, len(spec.Commands))
	for name := range spec.Commands {
		if !aliasSet[name] {
			names = append(names, name)
		}
	}
	sort.Strings(names)

	var cmds []commandData
	for _, name := range names {
		entry := spec.Commands[name]

		var flagKeys, boolFlags, repeatKeys []string
		for _, f := range entry.Flags {
			switch f.Type {
			case "bool":
				flagKeys = append(flagKeys, f.Name)
				boolFlags = append(boolFlags, f.Name)
			case "repeatable":
				repeatKeys = append(repeatKeys, f.Name)
			default:
				flagKeys = append(flagKeys, f.Name)
			}
		}

		noParams := len(flagKeys) == 0 && len(repeatKeys) == 0 && entry.Positional == ""

		cmds = append(cmds, commandData{
			Name:          name,
			Method:        entry.Method,
			NoParams:      noParams,
			FlagKeys:      flagKeys,
			BoolFlags:     boolFlags,
			RepeatKeys:    repeatKeys,
			PositionalKey: entry.Positional,
		})
	}

	tmpl, err := template.New("").Parse(fileTemplate)
	if err != nil {
		return nil, err
	}

	var buf strings.Builder
	if err := tmpl.Execute(&buf, templateData{Commands: cmds}); err != nil {
		return nil, err
	}

	formatted, err := format.Source([]byte(buf.String()))
	if err != nil {
		return nil, fmt.Errorf("gofmt: %w\n\nraw output:\n%s", err, buf.String())
	}
	return formatted, nil
}

// commandOverrideDef is the subset of override fields the generator needs to
// know about at generation time (currently none — overrides are applied at
// runtime in init()). The type exists here so the generator can import the
// same definition if needed in future.
type commandOverrideDef struct{}

func main() {
	socketPath, err := resolveSocket()
	if err != nil {
		fmt.Fprintf(os.Stderr, "cmuxd-remote gen: %v\n", err)
		fmt.Fprintf(os.Stderr, "Run with the cmux app open, or set CMUX_SOCKET_PATH.\n")
		os.Exit(1)
	}

	spec, err := fetchSpec(socketPath)
	if err != nil {
		fmt.Fprintf(os.Stderr, "cmuxd-remote gen: fetch spec: %v\n", err)
		os.Exit(1)
	}

	src, err := generate(spec, nil)
	if err != nil {
		fmt.Fprintf(os.Stderr, "cmuxd-remote gen: generate: %v\n", err)
		os.Exit(1)
	}

	outPath := filepath.Join(filepath.Dir(os.Args[0]), "..", "commands_generated.go")
	// When run via `go run ./gen/main.go` the binary is in a temp dir;
	// write relative to the source file instead.
	outPath = "commands_generated.go"

	if err := os.WriteFile(outPath, src, 0644); err != nil {
		fmt.Fprintf(os.Stderr, "cmuxd-remote gen: write %s: %v\n", outPath, err)
		os.Exit(1)
	}
	fmt.Printf("wrote %s (%d bytes)\n", outPath, len(src))
}
