import AppKit

nonisolated func cmuxGhosttyInputMods(from flags: NSEvent.ModifierFlags) -> ghostty_input_mods_e {
    var mods = GHOSTTY_MODS_NONE.rawValue
    if flags.contains(.shift) { mods |= GHOSTTY_MODS_SHIFT.rawValue }
    if flags.contains(.control) { mods |= GHOSTTY_MODS_CTRL.rawValue }
    if flags.contains(.option) { mods |= GHOSTTY_MODS_ALT.rawValue }
    if flags.contains(.command) { mods |= GHOSTTY_MODS_SUPER.rawValue }

    let rawFlags = flags.rawValue
    if rawFlags & UInt(NX_DEVICERSHIFTKEYMASK) != 0 { mods |= GHOSTTY_MODS_SHIFT_RIGHT.rawValue }
    if rawFlags & UInt(NX_DEVICERCTLKEYMASK) != 0 { mods |= GHOSTTY_MODS_CTRL_RIGHT.rawValue }
    if rawFlags & UInt(NX_DEVICERALTKEYMASK) != 0 { mods |= GHOSTTY_MODS_ALT_RIGHT.rawValue }
    if rawFlags & UInt(NX_DEVICERCMDKEYMASK) != 0 { mods |= GHOSTTY_MODS_SUPER_RIGHT.rawValue }

    return ghostty_input_mods_e(rawValue: mods)
}

nonisolated func cmuxGhosttyModifierFlagsByRemovingCommand(
    from flags: NSEvent.ModifierFlags
) -> NSEvent.ModifierFlags {
    var rawFlags = flags.rawValue
    rawFlags &= ~NSEvent.ModifierFlags.command.rawValue
    rawFlags &= ~UInt(NX_DEVICELCMDKEYMASK)
    rawFlags &= ~UInt(NX_DEVICERCMDKEYMASK)
    return NSEvent.ModifierFlags(rawValue: rawFlags)
}

