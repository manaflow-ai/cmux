import Foundation

/// A named command that can be submitted to an interactive terminal on demand.
public struct SavedTerminalCommand: Identifiable, Sendable, Equatable, SettingCodable {
    /// Stable identity used when editing or deleting the command.
    public var id: String

    /// User-facing name shown in Settings and the command palette.
    public var name: String

    /// Shell input submitted to the terminal, including any embedded newlines.
    public var command: String

    /// Creates a saved terminal command.
    ///
    /// - Parameters:
    ///   - id: Stable identity. A new UUID string is generated by default.
    ///   - name: User-facing command name.
    ///   - command: Shell input submitted when the command runs.
    public init(id: String = UUID().uuidString, name: String, command: String) {
        self.id = id
        self.name = name
        self.command = command
    }

    /// Decodes a command from its property-list representation.
    public static func decodeFromUserDefaults(_ raw: Any?) -> Self? { decode(raw) }

    /// Encodes the command as a property-list dictionary.
    public func encodeForUserDefaults() -> Any { encode() }

    /// Decodes a command from its JSON object representation.
    public static func decodeFromJSON(_ raw: Any?) -> Self? { decode(raw) }

    /// Encodes the command as a JSON object dictionary.
    public func encodeForJSON() -> Any { encode() }

    private static func decode(_ raw: Any?) -> Self? {
        guard let value = raw as? [String: Any],
              let id = value["id"] as? String,
              let name = value["name"] as? String,
              let command = value["command"] as? String else { return nil }
        return Self(id: id, name: name, command: command)
    }

    private func encode() -> Any {
        ["id": id, "name": name, "command": command]
    }
}
