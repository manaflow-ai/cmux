import Foundation

/// A render-path gate that can intentionally drop terminal frames.
public enum TerminalRenderDropGate: UInt8, Sendable, Codable, CaseIterable {
    /// Output is behind a pending terminal input acknowledgement sequence.
    case pendingInputSeq = 1
    /// A terminal replay barrier is active and live output is being held back.
    case replayBarrier = 2
    /// Render-grid output is waiting for a full baseline frame.
    case baselineWait = 3
    /// A viewport resize barrier is active while geometry is being reconciled.
    case viewportBarrier = 4

    /// The bit used in compact gate-active snapshots.
    public var bit: Int {
        1 << Int(rawValue - 1)
    }
}

/// What resolved a previously detected terminal render stall.
public enum TerminalStallRecoveryCause: UInt8, Sendable, Codable, CaseIterable {
    /// A later frame caught the surface up and rendered normally.
    case catchupFrame = 1
    /// A replay acknowledgement cleared the barrier.
    case replayAck = 2
    /// A terminal output resync was triggered.
    case resync = 3
    /// The user explicitly requested a refresh or render reset.
    case manualRefresh = 4
    /// The user or network path forced a reconnect.
    case reconnect = 5
    /// The surface detached, so the episode is no longer relevant.
    case surfaceDetached = 6
    /// A barrier cleared for a non-ack reason.
    case barrierCleared = 7
}

/// A stall monitor emission that callers can record or upload.
public enum TerminalStallEmission: Sendable, Equatable {
    /// A drop episode crossed the configured stall threshold.
    case stallDetected(
        surface: UInt32,
        gate: TerminalRenderDropGate,
        droppedFrames: Int,
        stallDuration: TimeInterval
    )
    /// A previously detected stall episode resolved.
    case stallRecovered(
        surface: UInt32,
        gate: TerminalRenderDropGate,
        how: TerminalStallRecoveryCause,
        duration: TimeInterval,
        droppedFrames: Int
    )
}

/// A point-in-time view of an open render drop episode.
public struct TerminalRenderStallSnapshot: Sendable, Equatable {
    /// The surface the episode belongs to.
    public let surface: UInt32
    /// The gate currently dropping frames.
    public let gate: TerminalRenderDropGate
    /// Number of dropped frames observed in the episode.
    public let droppedFrames: Int
    /// How long the episode has been open.
    public let duration: TimeInterval
    /// Whether ``TerminalStallEmission/stallDetected(surface:gate:droppedFrames:stallDuration:)`` already emitted.
    public let stallDetected: Bool
}

/// Tracks terminal render drop episodes without timers or shell dependencies.
///
/// Detection is drop-driven: a gate that keeps dropping frames crosses the
/// threshold on a later ``noteFrameDropped(surface:gate:now:)`` call. A fully
/// silent stream that stops producing frames entirely is intentionally outside
/// this type and is covered by the liveness and reconnect diagnostics.
public struct TerminalRenderStallMonitor: Sendable {
    private struct SurfaceGate: Hashable, Sendable {
        var surface: UInt32
        var gate: TerminalRenderDropGate
    }

    private struct Episode: Sendable {
        var startedAt: Date
        var droppedFrames: Int
        var detected: Bool
    }

    /// The elapsed time after which a drop episode becomes a stall.
    public var stallThreshold: TimeInterval

    private var episodes: [SurfaceGate: Episode] = [:]
    private var lastAppliedFrameAt: [UInt32: Date] = [:]

    /// Creates a render stall monitor.
    ///
    /// - Parameter stallThreshold: Minimum drop-episode age before emitting a
    ///   stall detection. Defaults to five seconds.
    public init(stallThreshold: TimeInterval = 5) {
        self.stallThreshold = stallThreshold
    }

    /// Records one frame dropped by a render gate.
    ///
    /// - Parameters:
    ///   - surface: Compact numeric surface handle.
    ///   - gate: Gate that dropped the frame.
    ///   - now: Caller-injected current time.
    /// - Returns: Emissions generated by the state transition.
    public mutating func noteFrameDropped(
        surface: UInt32,
        gate: TerminalRenderDropGate,
        now: Date
    ) -> [TerminalStallEmission] {
        let key = SurfaceGate(surface: surface, gate: gate)
        var episode = episodes[key] ?? Episode(startedAt: now, droppedFrames: 0, detected: false)
        episode.droppedFrames += 1
        let duration = now.timeIntervalSince(episode.startedAt)
        if !episode.detected, duration >= stallThreshold {
            episode.detected = true
            episodes[key] = episode
            return [.stallDetected(
                surface: surface,
                gate: gate,
                droppedFrames: episode.droppedFrames,
                stallDuration: duration
            )]
        }
        episodes[key] = episode
        return []
    }

    /// Records that a frame applied successfully and closes open episodes.
    ///
    /// A painted frame proves every render gate for the surface stopped
    /// dropping output, so this intentionally resolves all open gate episodes.
    ///
    /// - Parameters:
    ///   - surface: Compact numeric surface handle.
    ///   - now: Caller-injected current time.
    /// - Returns: Recovery emissions for episodes that had crossed the stall threshold.
    public mutating func noteFrameApplied(
        surface: UInt32,
        now: Date
    ) -> [TerminalStallEmission] {
        lastAppliedFrameAt[surface] = now
        return closeEpisodes(surface: surface, how: .catchupFrame, now: now)
    }

    /// Records that an external gate resolution closed one gate's episode.
    ///
    /// - Parameters:
    ///   - surface: Compact numeric surface handle.
    ///   - how: The recovery cause.
    ///   - now: Caller-injected current time.
    /// - Returns: Recovery emissions for episodes that had crossed the stall threshold.
    public mutating func noteGateResolved(
        surface: UInt32,
        gate: TerminalRenderDropGate,
        how: TerminalStallRecoveryCause,
        now: Date
    ) -> [TerminalStallEmission] {
        closeEpisode(surface: surface, gate: gate, how: how, now: now)
    }

    /// Records that a surface-level reset closed all open gate episodes.
    ///
    /// Use this only for recoveries that genuinely reset every gate for a
    /// surface, such as resync, reconnect, manual refresh, or detach.
    public mutating func noteSurfaceResolved(
        surface: UInt32,
        how: TerminalStallRecoveryCause,
        now: Date
    ) -> [TerminalStallEmission] {
        closeEpisodes(surface: surface, how: how, now: now)
    }

    /// Returns open episodes for one surface.
    public func snapshot(surface: UInt32, now: Date) -> [TerminalRenderStallSnapshot] {
        episodes.compactMap { key, episode in
            guard key.surface == surface else { return nil }
            return TerminalRenderStallSnapshot(
                surface: key.surface,
                gate: key.gate,
                droppedFrames: episode.droppedFrames,
                duration: now.timeIntervalSince(episode.startedAt),
                stallDetected: episode.detected
            )
        }
    }

    /// Returns active gates grouped by surface.
    public func activeGates(now: Date) -> [UInt32: [TerminalRenderDropGate]] {
        var result: [UInt32: [TerminalRenderDropGate]] = [:]
        for key in episodes.keys {
            result[key.surface, default: []].append(key.gate)
        }
        return result.mapValues { $0.sorted { $0.rawValue < $1.rawValue } }
    }

    /// Returns a compact bitmask of active gates for one surface.
    public func activeGateBitmask(surface: UInt32) -> Int {
        episodes.keys.reduce(0) { mask, key in
            key.surface == surface ? (mask | key.gate.bit) : mask
        }
    }

    /// Returns seconds since the last applied frame for a surface, when known.
    public func secondsSinceLastAppliedFrame(surface: UInt32, now: Date) -> TimeInterval? {
        lastAppliedFrameAt[surface].map { now.timeIntervalSince($0) }
    }

    private mutating func closeEpisodes(
        surface: UInt32,
        how: TerminalStallRecoveryCause,
        now: Date
    ) -> [TerminalStallEmission] {
        let keys = episodes.keys.filter { $0.surface == surface }
        var emissions: [TerminalStallEmission] = []
        emissions.reserveCapacity(keys.count)
        for key in keys.sorted(by: { $0.gate.rawValue < $1.gate.rawValue }) {
            guard let episode = episodes.removeValue(forKey: key),
                  episode.detected else {
                continue
            }
            emissions.append(.stallRecovered(
                surface: surface,
                gate: key.gate,
                how: how,
                duration: now.timeIntervalSince(episode.startedAt),
                droppedFrames: episode.droppedFrames
            ))
        }
        return emissions
    }

    private mutating func closeEpisode(
        surface: UInt32,
        gate: TerminalRenderDropGate,
        how: TerminalStallRecoveryCause,
        now: Date
    ) -> [TerminalStallEmission] {
        let key = SurfaceGate(surface: surface, gate: gate)
        guard let episode = episodes.removeValue(forKey: key),
              episode.detected else {
            return []
        }
        return [.stallRecovered(
            surface: surface,
            gate: gate,
            how: how,
            duration: now.timeIntervalSince(episode.startedAt),
            droppedFrames: episode.droppedFrames
        )]
    }
}
